# INTEGERS
print(30 + 10)
print(30 - 10)
print(30 / 10)
print(30 * 10)
print(30**2)
print( (30 + 2) * 7)


# FLOATS
print(0.5 + 0.5)
print(0.5 - 0.3)
print(0.6 / 0.3)
print(0.5 * 0.5)

# Division between two integers always returns a float number:
print(10 / 5)

# If you mix a float and an integer in any arithmetic operation, it returns a float:
print(11 + 2.0)

# UNDERSCORES IN NUMBERS
# To make large numbers more readable, you can group them with underscores
count = 10_000_000_000 # 10 billion
print(count)
