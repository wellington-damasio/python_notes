# Python supports the basic comparison operators
# - Less than (<)
# - Less or equal to (<=)
# - Greater than (>)
# - Greater or equal to (>=)
# - Equal to (==)
# - Not equal to (!=)

print(20 < 10) # true
print(20 > 10) # true
print(20 >= 20) # true
print(20 <= 20) # true

print(20 == 20) # true
print(20 == "20") # false



